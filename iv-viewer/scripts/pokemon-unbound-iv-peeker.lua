-- Main script logic
local PKMN_DS_SIZE = 100
local PARTY_ADDR = 0x02024284
local OPPONENT_PARTY_ADDR = 0x0202402C

local OFFSET_PV = 0
local OFFSET_OTID = 4
local OFFSET_NAME = 8
local OFFSET_ITEM = 34
local OFFSET_MOVES = 44
local OFFSET_EV = 56
local OFFSET_IV = 72

local buffer = console:createBuffer("iv-peeker")

local function readName(addr)
    local n1 = emu:read32(addr)
    local n2 = emu:read32(addr + 4)
    local n3 = emu:read16(addr + 8)
    local bytes = {
        n1 & 0xFF, (n1 >> 8) & 0xFF, (n1 >> 16) & 0xFF, (n1 >> 24) & 0xFF,
        n2 & 0xFF, (n2 >> 8) & 0xFF, (n2 >> 16) & 0xFF, (n2 >> 24) & 0xFF,
        n3 & 0xFF, (n3 >> 8) & 0xFF,
    }
    local name = ""
    for _, b in ipairs(bytes) do
        local ch = chars[b]
        if ch == nil then break end
        name = name .. ch
    end
    return name
end

local function printSlot(base, label)
    local pv = emu:read32(base + OFFSET_PV)
    if pv == 0 then return false end

    local pv_lo = pv & 0xFFFF
    local pv_hi = (pv >> 16) & 0xFFFF
    local tid = emu:read16(base + OFFSET_OTID)
    local sid = emu:read16(base + OFFSET_OTID + 2)
    local is_shiny = (tid ~ sid ~ pv_lo ~ pv_hi) < 16

    local name = readName(base + OFFSET_NAME)
    local nature = natures[pv % 25] or "?"
    local item = items[emu:read16(base + OFFSET_ITEM)] or "None"

    local m1 = moves[emu:read16(base + OFFSET_MOVES)] or "---"
    local m2 = moves[emu:read16(base + OFFSET_MOVES + 2)] or "---"
    local m3 = moves[emu:read16(base + OFFSET_MOVES + 4)] or "---"
    local m4 = moves[emu:read16(base + OFFSET_MOVES + 6)] or "---"

    local iv_data = emu:read32(base + OFFSET_IV)
    local iv = {
        iv_data & 0x1F,
        (iv_data >> 5) & 0x1F,
        (iv_data >> 10) & 0x1F,
        (iv_data >> 20) & 0x1F,
        (iv_data >> 25) & 0x1F,
        (iv_data >> 15) & 0x1F,
    }

    local ev1 = emu:read32(base + OFFSET_EV)
    local ev2 = emu:read16(base + OFFSET_EV + 4)
    local ev = {
        ev1 & 0xFF,
        (ev1 >> 8) & 0xFF,
        (ev1 >> 16) & 0xFF,
        ev2 & 0xFF,
        (ev2 >> 8) & 0xFF,
        (ev1 >> 24) & 0xFF,
    }

    local shiny = is_shiny and " ***SHINY***" or ""
    buffer:print(string.format("%s: %s%s\n", label, name, shiny))
    buffer:print(string.format("  Nature: %-8s | Item: %-16s | PV: 0x%08X\n", nature, item, pv))
    buffer:print(string.format("  IVs: %2d | %2d | %2d | %2d | %2d | %2d\n", iv[1], iv[2], iv[3], iv[4], iv[5], iv[6]))
    buffer:print(string.format("  EVs: %3d | %3d | %3d | %3d | %3d | %3d\n", ev[1], ev[2], ev[3], ev[4], ev[5], ev[6]))
    buffer:print(string.format("  Moves: %s | %s | %s | %s\n", m1, m2, m3, m4))
    return true
end

local function printAll()
    buffer:clear()
    buffer:moveCursor(0, 0)
    buffer:print("========================================\n")

    for i = 0, 5 do
        printSlot(OPPONENT_PARTY_ADDR + PKMN_DS_SIZE * i, "Opponent #" .. (i + 1))
    end

    for i = 0, 5 do
        printSlot(PARTY_ADDR + PKMN_DS_SIZE * i, "Party #" .. (i + 1))
    end

    buffer:print("========================================\n")
end

local lastPVs = {}
local frame = 0
local INTERVAL = 60

local function readAllPVs()
    local pvs = {}
    for i = 0, 5 do
        pvs[i] = emu:read32(OPPONENT_PARTY_ADDR + PKMN_DS_SIZE * i + OFFSET_PV)
    end
    for i = 0, 5 do
        pvs[i + 6] = emu:read32(PARTY_ADDR + PKMN_DS_SIZE * i + OFFSET_PV)
    end
    return pvs
end

local function pvsChanged(pvs)
    for k, v in pairs(pvs) do
        if lastPVs[k] ~= v then return true end
    end
    return false
end

function OnFrame()
    frame = frame + 1
    if frame % INTERVAL == 0 then
        local pvs = readAllPVs()
        if pvsChanged(pvs) then
            lastPVs = pvs
            printAll()
        end
    end
end

callbacks:add("frame", OnFrame)
